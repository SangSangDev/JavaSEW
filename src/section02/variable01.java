package section02;

/*
 * 1. 논리형(boolean)
 * true, false 두가지 값을 가지는 기본 자료형
 * 1byte
 * 
 * 
 */
public class variable01 {
	public static void main(String[] args) {
		boolean isWait;
		isWait = true;
		System.out.println("!!"+ isWait);
		
		boolean isRun = false;
		isRun = true;
		System.out.println(isRun);
		
		
	}
}
